package ex01;

public class ForEx5 {

	public static void main(String[] args) {
		
		
		/*
		 * 중첩 for문 - 구구단 출력
		 */
		for (int i = 1;  i<=5; i++) {
			System.out.println("----"+ i + "단----");
		for (int j = 1; j<=9; j++) {
			System.out.println(i+  "X" + j + "=" + i * j);}

		
}


	}

	
	
	
	}

